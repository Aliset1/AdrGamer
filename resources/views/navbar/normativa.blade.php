Reglamento Super Smash Bros. Ultimate Gamer Fest (2022)


1.	Reglas de Juego
1.1.	Configuración
●	Stocks: 3
●	Tiempo: 5 min
●	Custom Balance: Off
●	Team Attack: On
●	Multiplicador de daño: 1.0x
●	Final Smash Meter: Off
●	Objetos: Off y Ninguno
●	Pausa: Off
●	Radar: Pequeño

●	Stage Morph: Off
●	Stage Hazards: Off
●	Bans de escenarios: 2 para Mejor de 3, 1 para mejor de 5


2.	Procedimiento para jugar un set


1.	Los jugadores eligen sus personajes. Cada jugador puede elegir si aplicar
Elección a ciegas (ver sección 3.5).
2.	Realizar piedra papel o tigera para elegir el primer escenario
3.	Los jugadores juegan el primer juego de un set.
4.	El ganador del game anterior anula 2 escenarios en un set al mejor de 3 y 1 en un set al mejor de 5 (cuando sea aplicable). El jugador que ha perdido el ultimo game elige el escenario para el siguiente.
5.	El ganador del game anterior decide si cambiar personaje.

6.	El perdedor del game anterior decide si cambiar personaje.
7.	Se juega el siguiente game.
8.	Repetir los pasos 4 a 7 hasta terminar el set.


3.	Reglas Adicionales
3.1.	Duración del Set
Todos los torneos serán con sets al mejor de 3, excepto final de ganadores, final de perdedores y la gran final que serán al mejor de 5.

3.2.	Cláusula de Escenarios (DSR)
Jugadores no deben elegir un escenario en que ya hayan ganado en el set.



