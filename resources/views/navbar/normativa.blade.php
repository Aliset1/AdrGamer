3.11.	Calentamientos
Periodos de Calentamientos, chequedo de botones, y “Calienta-manos” no deben exceder 30 segundos del reloj dentro del juego. Violar esta regla puede resultar en una eliminación automática a la discreción de el/los organizadores.


3.12.	Ayuda de “entrenadores”


Ayudar durante un set no está permitido. Si se verifica que ha habido ayuda durante un set, recibirán las penalidades listadas a continuación:
●	1ra infracción: Llamado de atención oral al jugador y al entrenador. Esta advertencia se mantiene durante la duración de todo el torneo.
●	2da infracción: El jugador se penaliza con la pérdida de un game.
●	3rd	infracción:	El	jugador	y	el	entrenador	serán	completamente descalificados del torneo.

3.13.	Llegadas tarde

Cualquiera que no se presente 10 minutos después del tiempo de llamado está sujeto a una descalificación total del torneo.
 








3.14.	Interferencias de Controles
Si tu control está causando interrupciones en un torneo (durante un game o en otro momento), está sujeto a una descalificación total del torneo. Desconecta, apaga o desarma tu control inalámbrico al dejar una consola después de un set.

3.15.	Complicidad
Jugadores sospechosos de complicidad pueden ser inmediatamente descalificados del torneo. Incluye intencionalmente perder un game, compartir/repartir premios, o cometer cualquier otro tipo de manipulación de la llave del torneo. Los organizadores se reservan el derecho de entregar los premios si algún jugador en cuestión	es	sospechoso	de	complicidad.

3.16.	Malinterpretación/Configuración  errónea


Games o sets no serán re-jugados debido a una malinterpretación de las reglas o una configuración errónea de las reglas dentro del juego. Las reglas de juego deberán ser configuradas dependiendo de la sección 1.1. Está a cargo de la responsabilidad de los jugadores a preguntar cualquier duda de una regla en caso de desacuerdo entre jugadores, y el resultado de un game o set no será cambiado salvo ante circunstancias extremas, juzgadas por los organizadores del torneo.

3.17.	Reglas Finales


Si cualquier situación no antes prevista ocurre, el juicio de los organizadores es final. Las reglas pueden ser alteradas entre distintas fases de un torneo según los distintos intereses del mismo. (Ej: Un bug que cambia/rompe la jugabilidad de un escenario se descubre durante un torneo y puede ser explotado para alterar la jugabilidad normal del mismo. Entonces, el escenario podría ser removido de la lista de escenarios legales por el resto del evento.)
