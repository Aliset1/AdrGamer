2.	Procedimiento para jugar un set


1.	Los jugadores eligen sus personajes. Cada jugador puede elegir si aplicar
Elección a ciegas (ver sección 3.5).
2.	Realizar piedra papel o tigera para elegir el primer escenario
3.	Los jugadores juegan el primer juego de un set.
4.	El ganador del game anterior anula 2 escenarios en un set al mejor de 3 y 1 en un set al mejor de 5 (cuando sea aplicable). El jugador que ha perdido el ultimo game elige el escenario para el siguiente.
5.	El ganador del game anterior decide si cambiar personaje.

6.	El perdedor del game anterior decide si cambiar personaje.
7.	Se juega el siguiente game.
8.	Repetir los pasos 4 a 7 hasta terminar el set.




3.	Reglas Adicionales
3.1.	Duración del Set
Todos los torneos serán con sets al mejor de 3, excepto final de ganadores, final de perdedores y la gran final que serán al mejor de 5.
 


3.2.	Cláusula de Escenarios (DSR)
Jugadores no deben elegir un escenario en que ya hayan ganado en el set.


3.3.	Elección a ciegas



Cualquier jugador puede pedir elección de personajes a ciegas. En esta situación, un organizador o un tercero será avisado, en secreto, de la elección de personajes
de cada jugador. Ambos eligen su personaje para el primer game, con el organizador validando sus elecciones.

3.4.	Cláusula de Luchadores Mii
Luchadores Mii deben ser creados usando los “Mii Invitados” y su nombre debe contener los movimientos elegidos en formato “xxxx” (Ej. 2132). Cualquier combinación puede ser utilizada y cambiar movimientos deberá tratarse como cambiar de personaje.

3.5.	Pausa
Pausa debería estar en off. Sin embargo, si no lo está, pausar es válido únicamente cuando cada jugador permanezca en su PROPIA plataforma después de haber perdido una vida, y únicamente para llamar a un organizador o en el caso de un malfuncionamiento de un control. Toda otra pausa se penalizará con la pérdida de un stock para el jugador que ha pausado. Si al pausar el oponente pierde un stock,
el jugador que pause se penalizará con dos vidas.
 










3.6.	Campear
Campear excesivamente, o intencionalmente hacer el juego injugable está prohibido a la discreción de los organizadores del torneo. Campear incluye, pero no se limita a convertirse invisible, continuar combos infinitos por encima de 300%, y resguardarse en una posición donde tu oponente nunca puede acercarse. Campear se penalizará con la pérdida del game de quien inició la acción.

3.7.	Movimientos Autodestructivos


Si un game termina con un movimiento autodestructivo la pantalla de resultados determinará el ganador.

3.8.	Muerte Súbita


Si un game llega a Muerte Súbita, el ganador es determinado por los stocks y el daño de cada personaje al finalizar el game. Si ambos jugadores están empatados en stocks, el jugador con el menor daño será el ganador. En el caso de empate en daño, o un game en donde ambos jugadores pierden su última vida simultáneamente, se jugará un game de 1 vida con un tiempo límite de 3 minutos. Si esto volviera a pasar, se repite el proceso anteriormente descripto. El resultado de Muerte Súbita no se tomará en cuenta.

3.9.	Doble Identidad


Serás inmediatamente descalificado de un torneo sin devoluciones de inscripción si usas tags alternativos o escondes tu identidad para manipular la llave/ranking. La única excepción a esta regla se da si un jugador avisa a los organizadores con anticipación (antes de que inicie el torneo).

3.10.	Selección Color de Personaje
Si hay una disputa por el color de personajes o del equipo (ej. dos jugadores quieren usar Donkey Kong rosa), los jugadores jugaran un juego de piedra-papel- tijeras para determinar quien obtiene el color en cuestión.
 








